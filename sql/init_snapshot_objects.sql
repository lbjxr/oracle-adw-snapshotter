-- Base initialization for snapshot metadata objects.
-- The runtime can auto-create per-job snapshot tables if missing,
-- but this script gives you stable metadata tables up front.

CREATE TABLE SNAPSHOT_JOB_RUNS (
    RUN_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    JOB_NAME VARCHAR2(200) NOT NULL,
    TARGET_TABLE VARCHAR2(200) NOT NULL,
    STARTED_AT_UTC TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    FINISHED_AT_UTC TIMESTAMP WITH TIME ZONE,
    STATUS VARCHAR2(50) NOT NULL,
    ROW_COUNT NUMBER,
    ERROR_MESSAGE CLOB
);

CREATE TABLE SNAPSHOT_JOB_DEFS (
    JOB_NAME VARCHAR2(200) PRIMARY KEY,
    TARGET_TABLE VARCHAR2(200) NOT NULL,
    ENABLED CHAR(1) DEFAULT 'Y' CHECK (ENABLED IN ('Y', 'N')),
    DESCRIPTION VARCHAR2(1000),
    UPDATED_AT_UTC TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE SNAPSHOT_SCHEDULE_RUNS (
    SCHEDULE_RUN_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SCHEDULE_NAME VARCHAR2(200) NOT NULL,
    PLANNED_AT_UTC TIMESTAMP WITH TIME ZONE NOT NULL,
    STARTED_AT_UTC TIMESTAMP WITH TIME ZONE,
    FINISHED_AT_UTC TIMESTAMP WITH TIME ZONE,
    RANDOM_VALUE NUMBER(3),
    STATUS VARCHAR2(50) NOT NULL,
    SUCCESS_FLAG CHAR(1) CHECK (SUCCESS_FLAG IN ('Y', 'N')),
    READ_COUNT NUMBER,
    WROTE_COUNT NUMBER,
    SUMMARY VARCHAR2(2000),
    ERROR_MESSAGE CLOB,
    CREATED_AT_UTC TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE UNIQUE INDEX UQ_SNAPSHOT_SCHEDULE_RUNS_SLOT
    ON SNAPSHOT_SCHEDULE_RUNS (SCHEDULE_NAME, PLANNED_AT_UTC);

CREATE TABLE SNAPSHOT_SCHEDULE_LOGS (
    LOG_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SCHEDULE_RUN_ID NUMBER,
    SCHEDULE_NAME VARCHAR2(200) NOT NULL,
    PLANNED_AT_UTC TIMESTAMP WITH TIME ZONE NOT NULL,
    ACTUAL_AT_UTC TIMESTAMP WITH TIME ZONE NOT NULL,
    RANDOM_VALUE NUMBER(3) NOT NULL,
    READ_SOURCE_TABLE VARCHAR2(200),
    READ_ROW_COUNT NUMBER,
    READ_SUMMARY VARCHAR2(2000),
    PAYLOAD_JSON CLOB CHECK (PAYLOAD_JSON IS JSON),
    CREATED_AT_UTC TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL
);
